package day3;

import java.util.Scanner;

public class Prob1 {
	public static void main(String[] args) {

        /*
        다음은 대문자를 소문자로 변경하는 코드입니다.
        변수 ch에 저장된 문자가 대문자 인 경우에만 
	소문자로 변경하는 코드를 완성 합니다.
  	*/
	char ch = 'P';
	char lowerCase = (65<=ch && ch<=90)? (char)(ch+32) : ch;
	System.out.print("ch:"+ch);
	System.out.println(" to lowerCase :"+lowerCase);


/*
 아래는 변수 num의 값에따라  양수 음수 0을 출력하는 코드입니다.
삼항 연산자를 이용해서 에 알맞은 코드를 완성하세요. 
*/
	Scanner input = new Scanner(System.in);
	System.out.println("숫자를 입력하세요. _ ");
	String n = input.nextLine();
	int num = Integer.parseInt(n);
	String result = (num>0)? "양수" : (num<0)? "음수": "0" ;
	System.out.println(result);
	

	
	System.out.println(0.1f==0.1f);
	System.out.println(0.1f==0.1d);
	System.out.println(0.1d==0.1f);
	System.out.println(0.1d==0.1d);
	
	
	}
}          
